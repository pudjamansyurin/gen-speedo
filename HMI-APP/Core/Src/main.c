/**
 * @brief  Function implementing the ManagerTask thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartManagerTask */
void StartManagerTask(void *argument)
{
  /* USER CODE BEGIN 5 */
  TickType_t lastWake;

  // Reset database
  _LcdPower(0);
  _FlushData();

  // suspend other threads
//      osThreadSuspend(DisplayTaskHandle);
//      osThreadSuspend(CanTxTaskHandle);
//      osThreadSuspend(CanRxTaskHandle);

  // Release other threads
  osEventFlagsSet(GlobalEventHandle, EVENT_READY);

  /* Infinite loop */
  for (;;) {
    lastWake = osKernelGetTickCount();

    // Feed the dog
    HAL_IWDG_Refresh(&hiwdg);

    // reset display
    if (_GetTickMS() - VCU.d.tick.canRx > 5000)
      _FlushData();

    osDelayUntil(lastWake + 1000);
  }
  /* USER CODE END 5 */
}

